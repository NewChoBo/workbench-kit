import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const extensionsRoot = path.join(repoRoot, 'extensions');
const outputPath = path.join(
  repoRoot,
  'packages',
  'workbench-core',
  'src',
  'generated',
  'bundled-extensions.ts',
);

const extensionGroups = [
  {
    exportName: 'BUILTIN_WORKBENCH_EXTENSIONS',
    prefix: 'builtin.',
  },
  {
    exportName: 'SAMPLE_WORKBENCH_EXTENSIONS',
    prefix: 'samples.',
  },
];

const directoryEntries = await readdir(extensionsRoot, { withFileTypes: true });
const extensionDirectories = directoryEntries
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)
  .sort((left, right) => left.localeCompare(right));

const groups = await Promise.all(
  extensionGroups.map(async ({ exportName, prefix }) => ({
    exportName,
    extensions: await readExtensionGroup(prefix),
  })),
);

const moduleImports = groups
  .flatMap(({ extensions }) => extensions)
  .map(
    ({ importName, modulePath }) => `import * as ${importName} from ${JSON.stringify(modulePath)};`,
  );

const content = [
  "import type { WorkbenchExtensionDescription } from '../extension-registry.js';",
  ...moduleImports,
  '',
  '// This file is generated by scripts/bundle-workbench-extensions.mjs.',
  '// Do not edit it by hand.',
  '',
  ...groups.flatMap(({ exportName, extensions }) => [
    `export const ${exportName} = [`,
    ...extensions.map(formatExtensionDescription),
    `] as unknown as readonly WorkbenchExtensionDescription[];`,
    '',
  ]),
].join('\n');

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, await formatTypeScript(`${content.trimEnd()}\n`));

async function readExtensionGroup(prefix) {
  const groupDirectories = extensionDirectories.filter((directory) => directory.startsWith(prefix));

  return await Promise.all(
    groupDirectories.map(async (directory) => {
      const extensionPath = path.join('extensions', directory).replaceAll(path.sep, '/');
      const manifestPath = path.join(extensionsRoot, directory, 'workbench.extension.json');
      const manifest = JSON.parse(await readFile(manifestPath, 'utf8'));
      const importName = toModuleImportName(directory);
      const modulePath = path
        .relative(path.dirname(outputPath), path.join(extensionsRoot, directory, 'src', 'index.ts'))
        .replaceAll(path.sep, '/');

      return {
        extensionPath,
        importName,
        manifest,
        modulePath: modulePath.startsWith('.') ? modulePath : `./${modulePath}`,
      };
    }),
  );
}

function formatExtensionDescription({ extensionPath, importName, manifest }) {
  return [
    '  {',
    `    extensionPath: ${JSON.stringify(extensionPath)},`,
    `    manifest: ${formatManifest(manifest, 4)},`,
    `    module: ${importName},`,
    '  },',
  ].join('\n');
}

function formatManifest(manifest, indent) {
  const padding = ' '.repeat(indent);
  return JSON.stringify(manifest, null, 2).replaceAll('\n', `\n${padding}`);
}

function toModuleImportName(directory) {
  const words = directory.split(/[^a-zA-Z0-9]+/).filter(Boolean);
  const [first = 'extension', ...rest] = words;
  const suffix = rest.map((word) => `${word[0].toUpperCase()}${word.slice(1)}`).join('');

  return `${first}${suffix}Module`;
}

async function formatTypeScript(content) {
  try {
    const prettier = await import('prettier');

    return await prettier.format(content, {
      parser: 'typescript',
      printWidth: 100,
      singleQuote: true,
      trailingComma: 'all',
    });
  } catch {
    return content;
  }
}
